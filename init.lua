require 'fredriksvahn/options'
require 'fredriksvahn/keymaps'
require 'fredriksvahn/autocmds'

vim.keymap.set('n', '<leader>§', function()
  vim.opt.runtimepath:append(vim.fn.expand '~/private-dev/nvim-plugins/joplin.nvim')
  for name, _ in pairs(package.loaded) do
    if name:match '^joplin_nvim' then
      package.loaded[name] = nil
    end
  end
  require('joplin_nvim').setup()
  print 'Reloaded joplin.nvim!'
end)
vim.api.nvim_create_autocmd('TextYankPost', {
  desc = 'Highlight when yanking (copying) text',
  group = vim.api.nvim_create_augroup('kickstart-highlight-yank', { clear = true }),
  callback = function()
    vim.highlight.on_yank()
  end,
})

